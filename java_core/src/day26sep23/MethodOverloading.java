package day26sep23;

public class MethodOverloading {
	
	public static void main(String[] args) {
		System.out.println();  // printing new line
		
		System.out.println("Cyberv Success"); //printing string
		
		System.out.println(true); // printing boolean value
		
		System.out.println('Z');
		System.out.println(10.11f); //printing float value
		
		System.out.println(20.23); //printing double value
		System.out.println(100);
		byte b =1;
		System.out.println(b); //printing byte value
		
		System.out.println();
		//System: final class
		//out: is an object of PrintStream class -public final static
		//println - a method from PrintStream class
		
		System.out.println("RAJ");
	}
}
